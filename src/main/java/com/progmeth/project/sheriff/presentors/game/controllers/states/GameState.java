package com.progmeth.project.sheriff.presentors.game.controllers.states;

/**
 * Game state
 */
public  class GameState {
    /**
     * count
     */
    final int count;

    /**
     * Constructor
     * @param count count
     */
    public GameState(int count) {
        this.count = count;
    }

    /**
     * get count
     * @return count
     */
    public int getCount() {
        return count;
    }
}
